                                Klassiker wie die Sandgrube oder die Kohlhauhütte, den Kohlweiher, das Zwergenwäldchen und noch viele andere. Je nach geplantem Angebot, der Witterung und dem Fitnesslevel der anwesenden Kinder wird 
